      <h2>WingTsun für Kinder</h2>
          <img src="resources/kwt_1.jpg" alt="Kids Wing Tsun" class="float-left mr-2 mb-1" style="display:block;max-width:40%;height:auto">
<p style="text-align: justify">Kids-WingTsun ist neben einem Schwimmkurs wohl eine der sinnvollsten Freizeitbeschäftigungen für Kinder in der heutigen Zeit, die noch dazu viel Spaß macht:
Durch ein außergewöhnliches und ganzheitliches Trainingskonzept lernt Ihr Kind Selbstverteidigung und Selbstbehauptung, so dass es in Streitsituationen schnellstmöglich ein angemessenes Verhalten erlernt – was wiederum den Eltern, Lehrern, Erzieherinnen und dem Kind selbst garantiert, dass es richtig gehandelt hat.<br><br>
Nur, wenn das Kind verfolgt, gemobbt oder körperlich bedrängt wird, setzt es unser einzigartiges, mehrstufiges Vorgehen ein, allerdings auch nur, um aus dieser Situation schnellstmöglich wieder herauszukommen und nicht, um den Aggressor zu besiegen oder gar zu verletzen.
Unsere Kinder wollen in erster Linie nicht kämpfen, sondern Gewalt aus dem Weg gehen – und dies wird in Rollenspielen immer wieder praktisch geübt. Nebenbei verbessern diverse Übungen durch Bewegungen, welche die Körpermitte überkreuzen bzw. mit beiden Händen ausgeführt werden, auch die motorischen Fähigkeiten ganz enorm.<br><br>

Abgerundet wird das Training durch theoretische Erklärungen und praktisches Umsetzen von Themen wie z.B. gute Umgangsformen, Aufmerksamkeit, Selbstdisziplin oder Hilfsbereitschaft (um nur einige zu nennen), eine je Unterrichtseinheit.</p><br>

      <h4>Wie wird Kids-WingTsun unterrichtet?</h4>
       <img src="resources/kwt_2.jpg" alt="Kids Wing Tsun" class="float-left mr-2 mb-1" style="display:block;max-width: 16%; height:auto">
<p style="text-align: justify">Das Lehrkonzept ist sehr strukturiert aufgebaut, wobei die Inhalte und Programme in kleinen und überschaubaren Gruppen vermittelt werden. Gruppenübergreifende Elemente wie zum Beispiel Bewegungszirkel, Reaktionsspiele, Schlagpolster- oder Gesundheitsübungen bringen Dynamik in die Gruppe, haben einen großen Lerneffekt und machen noch dazu viel Spaß.<br>
Zudem werden die Kinder durch ein einzigartiges Unterrichtskonzept individuell gefördert, sodass ein stetiges Hinzulernen und Weiterentwickeln garantiert ist. Darüber hinaus gehört ein selbständiges Wiederholen von diversen Übungen oder Rollenspielen, die auf die Alltagsrealität von Kindern zugeschnitten sind, zur erfolgreichen Unterrichtsmethodik der EWTO.</p><br><br>

<h4>Wer kann Kids-WingTsun erlernen?</h4>
<p style="text-align: justify">
Das Konzept ist ausgerichtet für die ca. 5 bis 12-Jährigen, wobei in einigen EWTO-Schulen Kinder auch schon mit 4 Jahren in das Training einsteigen können.
Gefällt es den Kindern, gibt es die Möglichkeit, nach dem Durchlaufen der einzelnen Kinderprogramme mit ca. 12 Jahren in die Jugendgruppen der EWTO-Schulen zu wechseln.</p><br>

<h4>Das EWTO-Kids-WingTsun-Programm</h4>
<p style="text-align: justify">
Das Konzept darf nur von Ausbildern und Lehrern der EWTO unterrichtet werden, die sich zusätzlich in einer mehrtägigen Schulung die Qualifikation „Fachtrainer für Kids-WT“ erworben haben. Wir sind Fachtrainer für Kids-WingTsun.<br><br>
      
