      <h2>WingTsun – Kampfkunst aus China</h2>
      <img src="resources/wt_banner1.jpg" alt="Wing Tsun" class="float-left mr-2 mb-2 mt-2" style="display:block;max-width:40%;height:auto">
<p style="text-align: justify">WingTsun ist persönliche Weiterentwicklung. Es ist vielfältig und macht Spaß.
Es ist kein Wettkampfsport, sondern auf die Selbstverteidigung spezialisiert.
Der Legende nach von einer Frau erfunden, bedarf es nicht Kraft oder Akrobatik, um den Gegner zu besiegen, vielmehr nutzt man die Kraft des Angreifers und lenkt sie gegen ihn.<br><br>

Im Mittelpunkt steht eine einmalige und facettenreiche Technik, die nur durch konzentriertes Üben und bewusstes Sich-Einlassen erlernt werden kann. Fließende Bewegungen und plötzliche Schnelligkeit gehen in einem systematischen und koordinierten Bewegungsablauf ineinander über: Sie verschmelzen zu einer neuen und kraftvollen Einheit. Das systematische Erlernen von WingTsun steigert die körperliche und geistige Flexibilität und fördert die Wahrnehmungsfähigkeit. Den Weg dahin ergänzen gezielte Entspannungs- und Konzentrationstechniken sowie Atemübungen, die den Körper wieder „auftanken“.

<h4>Wie wird WingTsun unterrichtet?</h4>
<p style="text-align: justify">Der Schüler sollte versuchen regelmäßig ein bis zwei Mal wöchentlich über einen Zeitraum von mindestens einem halben Jahr hinweg zum Unterricht zu kommen, da man diese Zeit braucht, um bestimmte Verhaltensmuster unter Stress abrufen zu können.<br><br>

Zu Beginn des Unterrichts lernt der Schüler seinen Körper kennen, indem er bestimmte Abläufe (Formen, Grundtechniken) wiederholt, die ihm dann im weiteren Lernkontext zur Verfügung stehen.
Parallel dazu werden Partnerübungen eingebaut, die von Rollenspielen, über abgesprochene Partnerformen und Drillübungen, hin zu freien Angriffen führen, die die Schüler miteinander einüben. Dies erfolgt in einer angenehmen und entspannten Umgebung, da für neu zu erlernende Bewegungen ein stressfreier Zustand der Trainierenden notwendig ist.<br><br>

Der Unterrichtsstil ist dabei gerade anfangs ein auf Vorzeigen und Nachmachen basierender Gruppenunterricht. Der Ausbilder macht eine Übung mit dem Schüler oder einem Assistenten vor und die Gruppe wiederholt die Übung anschließend. Dabei sind die Gruppen selbst – je nach Übung und Aufgabenstellung – gemischt oder aber anhand ihrer Graduierung aufgeteilt.<br><br>
Die Rolle des Lehrers ist dann die desjenigen, der die äußeren Bedingungen dafür stellt, dass Selbstlernen möglich ist (vgl. Prof. Kernspecht, Kampflogik 3, S. 138). Er erklärt einige Aspekte, sodass die Schüler die Bewegung miteinander aufnehmen können. Aber er lässt ihnen auch genügend Spielraum, ihre individuelle Antwort zu finden; denn das ist das Wichtigste: Jeder Mensch hat seine eigenen Maße und Möglichkeiten. Deshalb muss er sich die Prinzipien des WingTsun für seinen eigenen Körper nach und nach „zurechtschneidern“, wobei der Lehrer helfend zur Seite steht. So werden die Fähigkeiten des Einzelnen gezielt gefördert und ausgebaut.<br><br>
    Man übt die Bewegungen und Reaktionen zunächst konzentriert in einer partnerschaftlichen Unterrichtsatmosphäre. Zum Ende einer Stunde hin werden diese dann mittels kontrollierter Übungen unter Stresshormoneinfluss weiter eingeschliffen.<br></p>

      <h4>Wer kann WingTsun erlernen?</h4>
      <img src="resources/wt_banner2.jpg" alt="Wing Tsun" class="float-left mr-2 mb-2 mt-2" style="display:block;max-width:40%;height:auto">
<p style="text-align: justify">WingTsun ist für alle gleichermaßen geeignet – ob klein oder groß, jung oder alt. Die EWTO hat für viele Zielgruppen eigene Sparten entwickelt, um diese noch effektiver und individueller betreuen zu können.<br>

Im allgemeinen WingTsun-Unterricht für Erwachsene werden für Männer und Frauen spezifische Konfliktsituationen im Alltag dargestellt und geklärt, d.h. Selbstverteidigungssituationen im Rahmen des BlitzDefence-Programms (spezifisches Rollenverhalten für Mann und Frau) sind in den Unterricht integriert. Aber natürlich kommt auch die Bewegung, das Auspowern, nicht zu kurz! Weitere Informationen zu BlitzDefence für Männer finden Sie unter <a href="https://deine-selbstverteidigung.de/selbstverteidigung-maenner.html">www.deine-selbstverteidigung.de/selbstverteidigung-maenner</a>. Darüber hinaus bieten einige Schulen auch spezielle Frauenkurse an, die noch intensiver auf die eigenen Bedürfnisse eingehen. Weitere Informationen zu BlitzDefence für Frauen finden Sie unter <a href="https://deine-selbstverteidigung.de/selbstverteidigung-frauen.html">www.deine-selbstverteidigung.de/selbstverteidigung-frauen</a><br><br>

Durch seinen speziellen Fokus auf Konzepte im Unterricht, die alle persönlichen Einschränkungen des jeweiligen Schülers berücksichtigen, ist WingTsun besonders auch für Menschen mit körperlichen und/oder geistigen Einschränkungen (disabled) geeignet. Individuelle Lösungen können im Unterricht gefunden werden!<br><br>
Darüber hinaus arbeitet die EWTO an einem speziellen Konzept für Senioren, in dem die ebenfalls häufig vorhandenen Einschränkungen einbezogen werden können. Außerdem ist der Unterricht dort nicht primär auf körperlichen Aufbau ausgelegt, sondern beinhaltet verstärkt gesundheitliche Aspekte: Wie kann ich mich im Training bewegen, die gestellte Aufgabe nach einer funktionierenden Selbstverteidigung lösen, ohne dass ich mich verletze oder meinen Körper überbeanspruche? In diesem Zusammenhang hilft das ChiKung – die Gesundheitssparte der EWTO.<br><br>
      
